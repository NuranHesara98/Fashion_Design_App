const axios = require('axios');
const dotenv = require('dotenv');
const path = require('path');
const fs = require('fs');

// Load environment variables
dotenv.config({ path: path.resolve(__dirname, '.env') });

// Get API key
const apiKey = process.env.OPENAI_API_KEY;
console.log('API Key exists:', !!apiKey);
console.log('API Key starts with:', apiKey ? apiKey.substring(0, 7) : 'N/A');
console.log('API Key length:', apiKey ? apiKey.length : 0);

// Test image generation with DALL-E
async function testImageGeneration() {
  try {
    console.log('Testing DALL-E image generation...');
    
    const prompt = "A fashion design sketch for a casual silk dress for a birthday party. Elegant, modern, and chic style with colors that complement skin tone #9F8880. Night time occasion. Photorealistic, studio lighting, neutral background.";
    
    const response = await axios.post(
      'https://api.openai.com/v1/images/generations',
      {
        model: 'dall-e-2',
        prompt: prompt,
        n: 1,
        size: '1024x1024'
      },
      {
        headers: {
          'Authorization': `Bearer ${apiKey}`,
          'Content-Type': 'application/json'
        }
      }
    );
    
    console.log('Success! Image generated by DALL-E.');
    console.log('Image URL:', response.data.data[0].url);
    
    // Save the response to a file
    fs.writeFileSync(
      path.resolve(__dirname, 'dalle-response.json'),
      JSON.stringify(response.data, null, 2)
    );
    
    console.log('Response saved to dalle-response.json');
    return true;
  } catch (error) {
    console.error('Error generating image with DALL-E:');
    console.error(error.message);
    
    if (error.response) {
      console.error('Response status:', error.response.status);
      console.error('Response data:', JSON.stringify(error.response.data, null, 2));
      
      // Save the error response to a file
      fs.writeFileSync(
        path.resolve(__dirname, 'dalle-error.json'),
        JSON.stringify({
          status: error.response.status,
          data: error.response.data
        }, null, 2)
      );
      
      console.log('Error response saved to dalle-error.json');
    }
    return false;
  }
}

// Run the test
testImageGeneration();
